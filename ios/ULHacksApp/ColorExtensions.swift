import Foundation
import SwiftUI

extension Color {
    struct AppTheme {
        static var fadedRed: Color {
            return Color("FadedRed")
        }
        static var fadedBlue: Color {
            return Color("FadedBlue")
        }
        static var fadedGreen: Color {
            return Color("FadedGreen")
        }
        static var fadedRedHighlight: Color {
            return Color("FadedRedHighlight")
        }
        static var fadedGoldHighlight: Color {
            return Color("FadedGoldHighlight")
        }
        static var fadedGreenHighlight: Color {
            return Color("FadedGreenHighlight")
        }
    }
}
